    private void loadStudent(){
        
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();        
        model.setRowCount(0);   
        List<Student> students = StudentDAO.getAllStudents();
        for (Student student : students) {
            model.addRow(new Object[]{student.getId(), student.getName(), student.getDob(), student.getAddress()});
        }
    }
    
    private void searchStudent(){
        
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();        
        model.setRowCount(0);   
        List<Student> students = StudentDAO.searchStudents(txtSearch.getText());
        for (Student student : students) {
            model.addRow(new Object[]{student.getId(), student.getName(), student.getDob(), student.getAddress()});
        }
    }
