    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        loadStudent();
        
        tblStudents.getSelectionModel().addListSelectionListener(event -> {
            int selectedRow = tblStudents.getSelectedRow();
            if (selectedRow != -1) {
                txtStudentID.setText(tblStudents.getValueAt(selectedRow, 0).toString());
                txtName.setText(tblStudents.getValueAt(selectedRow, 1).toString());
                txtDOB.setText(tblStudents.getValueAt(selectedRow, 2).toString());
                txtAddress.setText(tblStudents.getValueAt(selectedRow, 3).toString());
            }
        });
        
    }//GEN-LAST:event_formWindowActivated

